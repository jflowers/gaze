// makeStat constructs a gocyclo.Stat for testing.
func makeStat(pkg, fn, file string, line, complexity int) gocyclo.Stat {
	return gocyclo.Stat{
		PkgName:    pkg,
		FuncName:   fn,
		Complexity: complexity,
		Pos:        token.Position{Filename: file, Line: line},
	}
}

// makeCoverMap constructs a coverMaps for testing from file:line -> pct pairs.
func makeCoverMap(entries map[coverKey]float64) coverMaps {
	base := make(map[coverKey]float64, len(entries))
	for k, v := range entries {
		base[coverKey{file: filepath.Base(k.file), line: k.line}] = v
	}
	return coverMaps{exact: entries, basename: base}
}

func TestComputeScores_BasicCRAP(t *testing.T) {
	stats := []gocyclo.Stat{
		makeStat("pkg", "Foo", "/src/foo.go", 10, 5),
	}
	cm := makeCoverMap(map[coverKey]float64{
		{file: "/src/foo.go", line: 10}: 80.0,
	})
	opts := DefaultOptions()

	scores := computeScores(stats, cm, opts)

	if len(scores) != 1 {
		t.Fatalf("expected 1 score, got %d", len(scores))
	}
	s := scores[0]
	if s.Package != "pkg" {
		t.Errorf("expected Package 'pkg', got %q", s.Package)
	}
	if s.Function != "Foo" {
		t.Errorf("expected Function 'Foo', got %q", s.Function)
	}
	if s.Complexity != 5 {
		t.Errorf("expected Complexity 5, got %d", s.Complexity)
	}
	if s.LineCoverage != 80.0 {
		t.Errorf("expected LineCoverage 80.0, got %f", s.LineCoverage)
	}
	expectedCRAP := Formula(5, 80.0)
	if math.Abs(s.CRAP-expectedCRAP) > 0.001 {
		t.Errorf("expected CRAP %f, got %f", expectedCRAP, s.CRAP)
	}
}

