func TestComputeScores_GazeCRAP(t *testing.T) {
	stats := []gocyclo.Stat{
		makeStat("pkg", "Foo", "/src/foo.go", 10, 10),
	}
	cm := makeCoverMap(map[coverKey]float64{
		{file: "/src/foo.go", line: 10}: 90.0,
	})
	opts := DefaultOptions()
	opts.ContractCoverageFunc = func(pkg, fn string) (ContractCoverageInfo, bool) {
		if pkg == "pkg" && fn == "Foo" {
			return ContractCoverageInfo{Percentage: 75.0}, true
		}
		return ContractCoverageInfo{}, false
	}

	scores := computeScores(stats, cm, opts)

	if len(scores) != 1 {
		t.Fatalf("expected 1 score, got %d", len(scores))
	}
	s := scores[0]
	if s.ContractCoverage == nil {
		t.Fatal("expected non-nil ContractCoverage")
	}
	if *s.ContractCoverage != 75.0 {
		t.Errorf("expected ContractCoverage 75.0, got %f", *s.ContractCoverage)
	}
	if s.GazeCRAP == nil {
		t.Fatal("expected non-nil GazeCRAP")
	}
	expectedGazeCRAP := Formula(10, 75.0)
	if math.Abs(*s.GazeCRAP-expectedGazeCRAP) > 0.001 {
		t.Errorf("expected GazeCRAP %f, got %f", expectedGazeCRAP, *s.GazeCRAP)
	}
	if s.Quadrant == nil {
		t.Fatal("expected non-nil Quadrant")
	}
}

